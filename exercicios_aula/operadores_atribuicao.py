# atribuição simples
x = 5
print (x)

# adição atribuição
x +=1
print(x)

# subtração atribuição
x -=3
print(x)

# multiplicação
multiplicacao = 4
multiplicacao *=3
print(multiplicacao)

# divisão
divisao = 121
divisao /=11
print(divisao)

# divisão inteira
divisao_inteira = 10
divisao_inteira //=3
print(divisao_inteira)

# módulo na atribuição
modulo = 10
modulo %=3
print(modulo)

# exponenciação
exponenciacao = 5
exponenciacao **= 2
print(exponenciacao)


